export function setCurrentLanguage(language: UiLanguage) {
  currentLanguage = language;
}

export function getCurrentLanguage(): UiLanguage {
  return currentLanguage;
}

export function isPortugueseLanguage(language: UiLanguage = currentLanguage): boolean {
  return language === "pt-BR";
}

function currencyFormatter(language: UiLanguage) {
  return new Intl.NumberFormat(language, {
    style: "currency",
    currency: UI_CURRENCY,
    minimumFractionDigits: 2,
  });
}

function dateFormatter(language: UiLanguage) {
  return new Intl.DateTimeFormat(language, {
    month: "short",
    day: "numeric",
    year: "numeric",
  });
}

function dateTimeFormatter(language: UiLanguage) {
  return new Intl.DateTimeFormat(language, {
    month: "short",
    day: "numeric",
    year: "numeric",
    hour: "2-digit",
    minute: "2-digit",
  });
}

function relativeTimeFormatter(language: UiLanguage) {
  return new Intl.RelativeTimeFormat(language, {
    numeric: "auto",
  });
}

export function formatCurrencyFromCents(cents: number): string {
  return currencyFormatter(currentLanguage).format(cents / 100);
}

export function formatLocaleDate(date: Date | string): string {
  return dateFormatter(currentLanguage).format(new Date(date));
}

export function formatLocaleDateTime(date: Date | string): string {
  return dateTimeFormatter(currentLanguage).format(new Date(date));
}

export function formatRelativeTimeFromNow(date: Date | string): string {
  const now = Date.now();
  const then = new Date(date).getTime();
  const seconds = Math.round((then - now) / 1000);
  const absSeconds = Math.abs(seconds);

  if (absSeconds < 60) {
    return isPortugueseLanguage() ? "agora mesmo" : "just now";
  }

  const formatter = relativeTimeFormatter(currentLanguage);
  if (absSeconds < 3600) return formatter.format(Math.round(seconds / 60), "minute");
  if (absSeconds < 86400) return formatter.format(Math.round(seconds / 3600), "hour");
  if (absSeconds < 604800) return formatter.format(Math.round(seconds / 86400), "day");
  if (absSeconds < 2_592_000) return formatter.format(Math.round(seconds / 604800), "week");
  return formatter.format(Math.round(seconds / 2_592_000), "month");
}

export function translateStatus(status: string): string {
  return statusLabels[currentLanguage][status] ?? status.replace(/_/g, " ");
}

export function translateAgentRole(role: string): string {
  return agentRoleLabels[currentLanguage][role] ?? role;
}

